    private void userList(){
   
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int qRows = model.getRowCount()-1;  //√çndices van de 0 a n-1
        //System.out.println("Tabla "+a);   //Para mostrar por consola el resultado
        for(int i=qRows;i>=0;i--){ 

            //System.out.println("i "+i);    //Para mostrar por consola el resultado
            model.removeRow(i);
        }
    
        UserService usersService = new UserService();
        ArrayList<User> users = usersService.findAll();
        Object[] object = new Object[6];
        for(User user:users){
            object[0] = user.getUserid();
            object[1] = user.getUsername();
            object[2] = user.getPassword();
            object[3] = user.getEmail();
            object[4] = user.getTheme();
            object[5] = user.getBackground();
            model.addRow(object);
        }
    };
            
