    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectedRow = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        UserService userService = new UserService();
        
        Long id = (Long) model.getValueAt(selectedRow, 0);
        System.out.print(id);
        User user = userService.findUser((int)(long) id);
        
        editId.setText(Long.toString(user.getUserid()));
        editPassword.setText(user.getPassword());
        editUsername.setText(user.getUsername());
        editEmail.setText(user.getEmail());
        editTheme.setText(user.getTheme());
        editBackground.setText(Integer.toString(user.getBackground()));
        deleteId.setText(Long.toString(user.getUserid()));
    }//GEN-LAST:event_jTable1MouseClicked

