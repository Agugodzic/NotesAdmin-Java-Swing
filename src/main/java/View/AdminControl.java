    private void editSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editSaveActionPerformed
        Integer id = Integer.valueOf(editId.getText());
        String password = editPassword.getText();
        String username = editUsername.getText();
        String email = editEmail.getText();
        String theme = editTheme.getText();
        Integer background = Integer.valueOf(editBackground.getText());
        
        User user = new User(id,username,password,email,theme,background);
        UserService userService = new UserService();
        userService.updateUser(user, id);
        userList();
    }//GEN-LAST:event_editSaveActionPerformed

