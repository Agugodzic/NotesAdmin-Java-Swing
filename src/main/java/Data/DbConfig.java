package Data;

public class DbConfig {
    private String dbname = "";
    private String password = "";
    private String user = "";

    private String host = "";
    private int port = 0;

    public String getDbname() {
        return dbname;
    }

    public String getPassword() {
        return password;
    }

    public String getUser() {
        return user;
    }

    public String getHost() {
        return host;
    }

    public int getPort() {
        return port;
    }
}
